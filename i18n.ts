// FIX: Export a type for translation keys for type safety
export type TranslationKey = keyof typeof translations.en;

export const getBrowserLanguage = (): Language => {
    const lang = navigator.language.split('-')[0];
    if (['en', 'es', 'zh', 'hi', 'fr', 'ru', 'pt'].includes(lang)) {
        return lang as Language;
    }
    return 'en'; // Default to English
};

export const getTranslator = (lang: Language) => {
    // FIX: Use the exported TranslationKey type
    return (key: TranslationKey): string => {
        return (translations[lang] as any)[key] || translations.en[key];
    };
};